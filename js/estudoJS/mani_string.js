
var nome = 'Ruan'

console.log(nome.charAt(0))//retorna o caracter na posição do indice
console.log(nome.concat(' Carlos'))//concatena variáveis
console.log(nome.indexOf('p'))//retorna o index de uma determinada letra, se a letra não estiver na variável, retorna -1
console.log(nome.replace('an', 'na'))//retorna o valor da variável substituido pelo segundo item passado como parâmetro
console.log(nome.split('a'))//retorna um objeto, e tirando o valor passado como parâmetro
console.log(nome.toUpperCase())//coloca todas as letra em maiusculas.
console.log(nome[0])//retorna a letra da posição 0
console.log(nome.repeat(2))//repete o valor o numero de vezes que é passado no parâmetro.




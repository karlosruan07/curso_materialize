

console.log(Math.round(3.50))//arredonda um valor, se a segunda casa decimal for entre 0 e 4 então irá arrendondar para baixo, se for entre 5 e 9 arredonda pra cima
console.log(Math.abs(5))//converte para um valor positivo
console.log(Math.sqrt(49))//retorna a raiz quadrada
console.log(Math.ceil(1.9))//arrendonda um valor decimal para cima
console.log(Math.floor(3.9))//arredonda para baixo
console.log(Math.PI)//retorna o valor de PI
console.log(Math.random())//retorna um valor entre 0 e 1
console.log(Math.pow(2,2))//retorna a potencia do primeiro valor elevado ao segundo valor
console.log(Math.max(1,2,3,4,5))//retorna o valor máximo
console.log(Math.min(1,2,3,4,5))//retorna o valor minimo




